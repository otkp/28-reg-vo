public class VcrValidationVo {
	
	private String taxType;
	
	private String prNo;
	
	private String vcrNum;
	
	private String covCode;

	public String getTaxType() {
		return taxType;
	}

	public void setTaxType(String taxType) {
		this.taxType = taxType;
	}

	public String getPrNo() {
		return prNo;
	}

	public void setPrNo(String prNo) {
		this.prNo = prNo;
	}

	public String getVcrNum() {
		return vcrNum;
	}

	public void setVcrNum(String vcrNum) {
		this.vcrNum = vcrNum;
	}

	public String getCovCode() {
		return covCode;
	}

	public void setCovCode(String covCode) {
		this.covCode = covCode;
	}

}
