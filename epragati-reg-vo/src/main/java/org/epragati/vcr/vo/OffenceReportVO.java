package org.epragati.vcr.vo;

public class OffenceReportVO {
	
	private String offenceDescription;
	
	private String amount1;
	
	private String offenceDescriptionPart;
	
	private String offenceRemark;
	
	public String getOffenceDescription() {
		return offenceDescription;
	}
	public void setOffenceDescription(String offenceDescription) {
		this.offenceDescription = offenceDescription;
	}
	public String getAmount1() {
		return amount1;
	}
	public void setAmount1(String amount1) {
		this.amount1 = amount1;
	}
	public String getOffenceDescriptionPart() {
		return offenceDescriptionPart;
	}
	public void setOffenceDescriptionPart(String offenceDescriptionPart) {
		this.offenceDescriptionPart = offenceDescriptionPart;
	}
	public String getOffenceRemark() {
		return offenceRemark;
	}
	public void setOffenceRemark(String offenceRemark) {
		this.offenceRemark = offenceRemark;
	}
	
	

}
