package org.epragati.rta.reports.vo;

public class QuarterTaxVO {

	private Double quarterlyTax;
	
	private Double quarterlyTaxPenality;
	
	private Double total;

	public Double getQuarterlyTax() {
		return quarterlyTax;
	}

	public void setQuarterlyTax(Double quarterlyTax) {
		this.quarterlyTax = quarterlyTax;
	}

	public Double getQuarterlyTaxPenality() {
		return quarterlyTaxPenality;
	}

	public void setQuarterlyTaxPenality(Double quarterlyTaxPenality) {
		this.quarterlyTaxPenality = quarterlyTaxPenality;
	}

	public Double getTotal() {
		return total;
	}

	public void setTotal(Double total) {
		this.total = total;
	}
}
