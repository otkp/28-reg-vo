package org.epragati.rta.reports.vo;

public class LifeTaxVO {
	
	private Double lifeTax;
	
	private Double lifeTaxPenality;
	
	private Double total;

	public Double getLifeTax() {
		return lifeTax;
	}

	public void setLifeTax(Double lifeTax) {
		this.lifeTax = lifeTax;
	}

	public Double getLifeTaxPenality() {
		return lifeTaxPenality;
	}

	public void setLifeTaxPenality(Double lifeTaxPenality) {
		this.lifeTaxPenality = lifeTaxPenality;
	}

	public Double getTotal() {
		return total;
	}

	public void setTotal(Double total) {
		this.total = total;
	}

}
